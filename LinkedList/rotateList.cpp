/*  This function should rotate list counter-clockwise
    by k and return new head (if changed) */
Node* rotate(Node* head, int k) {
    
    Node *head1 = head;
    Node *head2 = head;
    int cnt = 0;
    while(cnt<k){
        if(head2->next==NULL)
            return head;
        head2 = head2->next;
        cnt++;
    }
    head = head2;
    while(head2->next!=NULL)
        head2 = head2->next;
    head2->next = head1;
    
    while(head1->next!=head)
        head1=head1->next;
    head1->next = NULL;
    
    return head;
}

